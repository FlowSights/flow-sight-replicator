// Helper function to search locations
export const searchLocations = (query: string): typeof worldLocations => {
  if (!query || query.length === 0) return worldLocations.slice(0, 10);
  
  const lowerQuery = query.toLowerCase();
  return worldLocations
    .filter(loc => 
      loc.label.toLowerCase().includes(lowerQuery) ||
      loc.value.toLowerCase().includes(lowerQuery)
    )
    .slice(0, 15); // Limit results to 15 for performance
};
